public class Integer extends Number {
    void add(int a, int b) {
        System.out.println(a + b);
    }


    void minus(int a, int b) {
        System.out.println( a - b);
    }


    void multiplication(int a, int b) {
        System.out.println( a * b);
    }


    void division(int a, int b) {
        System.out.println(a / b);
    }
}