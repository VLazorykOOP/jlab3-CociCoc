public class Real extends Number{
    void add(double a, double b) {
        System.out.println(a + b);
    }


    void minus(double a, double b) {
        System.out.println( a - b);
    }


    void multiplication(double a, double b) {
        System.out.println( a * b);
    }


    void division(double a, double b) {
        System.out.println(a / b);
    }
}